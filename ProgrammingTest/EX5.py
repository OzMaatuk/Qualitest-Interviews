# OOP Principles:

# 1. Everything is an object (class).
# 2. Class includes: constructure, destructor, members and methods, with private/protacted/public privilages.
# 3. Class can inherit from other class and adapt it's capabilities.
# 4. Objects of class can be used in Polymorphizm way where they appears as thier base (inherited) class.
# 5. Class can be abstract, which will requier implicit implementation by other entity.